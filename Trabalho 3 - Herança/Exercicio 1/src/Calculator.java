public class Calculator {

    public double som(double v1, double v2) {
        return v1 + v2;
    }

    public double subtraction(double v1, double v2) {
        return v1 - v2;
    }

    public double divisor(double v1, double v2) {
        return v1 / v2;
    }

    public double multiplication(double v1, double v2) {
        return v1 * v2;
    }
}
